package com.netoperation.config.model;

public class ColorOptionBean {

    /**
     * dark : #313131
     * light : #ffffff
     */

    private String dark;
    private String light;

    public String getDark() {
        return dark;
    }

    public void setDark(String dark) {
        this.dark = dark;
    }

    public String getLight() {
        return light;
    }

    public void setLight(String light) {
        this.light = light;
    }
}
